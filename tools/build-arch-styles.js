const PROJECT_ROOT = path.join(__dirname, "..");
const STYLES_DIR = path.join(PROJECT_ROOT, "src/styles");
const OUTPUT = path.join(PROJECT_ROOT, "dist", "demiurge.css");

function buildArchCoreCss() {
  if (!fs.existsSync(STYLES_DIR)) {
    console.warn(`âš ï¸  No existe la carpeta "styles" en ${STYLES_DIR}.`);
    console.warn("    Se generarÃ¡ un arch-core.css vacÃ­o.\n");

    fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
    fs.writeFileSync(
      OUTPUT,
      "/* arch-core.css generado: no se encontraron estilos en /styles */\n",
      "utf8"
    );
    console.log(`âœ… arch-core.css generado en: ${OUTPUT}`);
    return;
  }

  const entries = fs.readdirSync(STYLES_DIR, { withFileTypes: true });

  const cssFiles = entries
    .filter((e) => e.isFile() && e.name.toLowerCase().endsWith(".css"))
    .map((e) => e.name)
    .sort();

  if (!cssFiles.length) {
    console.warn(`âš ï¸  No hay ficheros .css en ${STYLES_DIR}.`);
    fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
    fs.writeFileSync(
      OUTPUT,
      "/* arch-core.css generado: carpeta /styles vacÃ­a */\n",
      "utf8"
    );
    console.log(`âœ… arch-core.css generado en: ${OUTPUT}`);
    return;
  }

  let bundle = ASCII;

  for (const file of cssFiles) {
    const absPath = path.join(STYLES_DIR, file);
    const css = fs.readFileSync(absPath, "utf8");

    bundle += `/* ==== BEGIN ${file} ==== */\n`;
    bundle += css.trimEnd();
    bundle += `\n/* ==== END ${file} ==== */\n\n`;
  }

  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, bundle, "utf8");

  console.log("ðŸ“¦ Ficheros CSS incluidos desde /styles:");
  cssFiles.forEach((f) => console.log("   -", f));
  console.log(`\nâœ… arch-core.css generado en: ${OUTPUT}`);
}

